import streamlit as st

# –£–∫—Ä–∞—ó–Ω—Å—å–∫—ñ –≥–æ–ª–æ—Å–Ω—ñ —Ç–∞ –ø—Ä–∏–≥–æ–ª–æ—Å–Ω—ñ
vowels = "–∞–µ—î–∏—ñ—ó–æ—É—é—è–ê–ï–Ñ–ò–Ü–á–û–£–Æ–Ø"
consonants = "–±–≤–≥“ë–¥–∂–∑–π–∫–ª–º–Ω–ø—Ä—Å—Ç—Ñ—Ö—Ü—á—à—â–ë–í–ì“ê–î–ñ–ó–ô–ö–õ–ú–ù–ü–†–°–¢–§–•–¶–ß–®–©"

# –§—É–Ω–∫—Ü—ñ—è –¥–ª—è –ø—ñ–¥—Ä–∞—Ö—É–Ω–∫—É –≥–æ–ª–æ—Å–Ω–∏—Ö —ñ –ø—Ä–∏–≥–æ–ª–æ—Å–Ω–∏—Ö
def count_letters(text):
    vowel_count = 0
    consonant_count = 0

    for char in text:
        if char in vowels:
            vowel_count += 1
        elif char in consonants:
            consonant_count += 1

    return vowel_count, consonant_count

# –Ü–Ω—Ç–µ—Ä—Ñ–µ–π—Å Streamlit
st.title("üî° –ü—ñ–¥—Ä–∞—Ö—É–Ω–æ–∫ –≥–æ–ª–æ—Å–Ω–∏—Ö —ñ –ø—Ä–∏–≥–æ–ª–æ—Å–Ω–∏—Ö")
st.write("–í–≤–µ–¥—ñ—Ç—å —Ç–µ–∫—Å—Ç, –ø–æ—Ç—ñ–º –Ω–∞—Ç–∏—Å–Ω—ñ—Ç—å –∫–Ω–æ–ø–∫—É –¥–ª—è –∞–Ω–∞–ª—ñ–∑—É:")

# –ü–æ–ª–µ –¥–ª—è –≤–≤–µ–¥–µ–Ω–Ω—è —Ç–µ–∫—Å—Ç—É
user_input = st.text_area("–¢–µ–∫—Å—Ç:")

# –ö–Ω–æ–ø–∫–∞ –∑–∞–ø—É—Å–∫—É –ø—ñ–¥—Ä–∞—Ö—É–Ω–∫—É
if st.button("üîç –ü–æ—Ä–∞—Ö—É–≤–∞—Ç–∏"):
    if user_input.strip() != "":
        vowels_num, consonants_num = count_letters(user_input)
        st.subheader("üìä –†–µ–∑—É–ª—å—Ç–∞—Ç–∏ –∞–Ω–∞–ª—ñ–∑—É:")
        st.write(f"üî¥ –ö—ñ–ª—å–∫—ñ—Å—Ç—å –≥–æ–ª–æ—Å–Ω–∏—Ö: **{vowels_num}**")
        st.write(f"üîµ –ö—ñ–ª—å–∫—ñ—Å—Ç—å –ø—Ä–∏–≥–æ–ª–æ—Å–Ω–∏—Ö: **{consonants_num}**")
    else:
        st.warning("–ë—É–¥—å –ª–∞—Å–∫–∞, –≤–≤–µ–¥—ñ—Ç—å —Ç–µ–∫—Å—Ç –ø–µ—Ä–µ–¥ –ø—ñ–¥—Ä–∞—Ö—É–Ω–∫–æ–º.")
